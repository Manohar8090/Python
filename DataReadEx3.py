#Program for accepting two Numerical Values and find their Product
#DataReadEx3.py
x=input("Enter First Value:")
y=input("Enter Second Value:")
#convert x and y values in int type
a=float(x)
b=float(y)
#find product
c=a*b
#display the result
print("-"*50)
print("\t\tVal of a={}".format(a))
print("\t\tVal of b={}".format(b))
print("\t\tProduct={}".format(c))
print("-"*50)